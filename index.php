    // Disable form submissions if there are invalid fields
    (function() {
        'use strict';
        window.addEventListener('load', function() {
            // Get the forms we want to add validation styles to
            var forms = document.getElementsByClassName('needs-validation');
            // Loop over them and prevent submission
            var validation = Array.prototype.filter.call(forms, function(form) {
                form.addEventListener('submit', function(event) {
                    event.preventDefault();
                    if (form.checkValidity() === false) {
                        event.preventDefault();
                        event.stopPropagation();
                    } else {
                        document.getElementById('msgSubmit').innerHTML = '';
                        var data = JSON.stringify({
                        "name": $("#name").val(),
                        "email": $("#email").val(),
                        "phone": $("#phone").val(),
                        "message": $("#message").val()
                        });
                        var xhr = new XMLHttpRequest();
                        xhr.withCredentials = true;
                        xhr.addEventListener("readystatechange", function() {
                        if(this.readyState === 4) {
                            let response = JSON.parse(this.responseText)
                            if(response.status){
                                document.getElementById('msgSubmit').innerHTML = '<span class="text-success">'+response.message+'</span>';
                                form.reset();
                                form.classList.remove("was-validated");
                            }
                            else{
                                document.getElementById('msgSubmit').innerHTML = '<span class="text-danger">'+response.message+'</span>';
                            }
                        }
                        });

                        xhr.open("POST", "mail.php");
                        xhr.setRequestHeader("Content-Type", "application/json");
                        xhr.send(data);
                    }
                    form.classList.add('was-validated');

                }, false);
            });
        }, false);
    })();
